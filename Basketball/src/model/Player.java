package model;

public class Player {
	
	private  String name; 
	private int age; 
	private String team ; 
	
	private double per; 
	private double ts;
	private double orb;
	private double derb;
	private double g;
	
	public Player(String name, int age, String team, double per, double ts, double orb, double derb, double g) {
		super();
		this.name = name;
		this.age = age;
		this.team = team;
		this.per = per;
		this.ts = ts;
		this.orb = orb;
		this.derb = derb;
		this.g = g;
	}

	
	
	
}
